		<h2>Cara Menghapus Akun dan Data Anda</h2>
		<p>
			Saat ini, penghapusan akun dilakukan secara manual untuk memastikan keamanan. Jika Anda ingin
			menghapus akun Anda dari <strong>TEXY</strong> beserta semua data yang terkait (seperti nama, email, dan
			dokumen Notepad2), silakan ikuti langkah-langkah berikut:
		</p>

		<ol>
			<li>
				Kirimkan email ke <strong><a href="mailto:yusupmabd@gmail.com">yusupmabd@gmail.com</a></strong>.
			</li>
			<li>Gunakan subjek email: <strong>"Permintaan Penghapusan Akun TEXY"</strong>.</li>
			<li>
				Di dalam email, cantumkan <strong>alamat email yang terdaftar</strong> pada akun TEXY Anda (alamat yang
				Anda gunakan saat login dengan Facebook/Google).
			</li>
			<li>Jika memungkinkan, kirimkan email permintaan tersebut dari alamat email yang sama.</li>
		</ol>

		<h2>Apa yang terjadi setelah saya meminta penghapusan?</h2>
		<ul>
			<li>Kami akan memproses permintaan Anda dalam waktu maksimal 7 hari kerja.</li>
			<li>
				Semua data pribadi Anda, termasuk profil (nama, email) dan dokumen yang Anda buat di TEXY
				(misal: Notepad2), akan dihapus secara permanen dari database Supabase kami.
			</li>
			<li>Tindakan ini tidak dapat dibatalkan. Setelah dihapus, data tidak dapat dipulihkan.</li>
			<li>Kami akan membalas email Anda untuk mengonfirmasi bahwa penghapusan telah selesai.</li>
		</ul>

		<h2>Menghapus Akses Aplikasi di Facebook</h2>
		<p>
			Jika Anda login menggunakan Facebook dan ingin menghapus koneksi TEXY dari akun Facebook Anda, Anda juga dapat
			melakukannya melalui pengaturan Facebook:
		</p>
		<ol>
			<li>Buka Facebook dan masuk ke akun Anda.</li>
			<li>Buka <strong>Pengaturan & Privasi</strong> > <strong>Pengaturan</strong>.</li>
			<li>Pilih <strong>Aplikasi dan Situs Web</strong> di menu sebelah kiri.</li>
			<li>Cari aplikasi <strong>TEXY</strong>, klik <strong>Hapus</strong>, dan ikuti instruksi di layar.</li>
		</ol>
